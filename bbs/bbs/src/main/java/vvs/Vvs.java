package vvs;

public class Vvs {
	
	private int vvsID;
	private String vvsTitle;
	private String userID;
	private String vvsDate;
	private String vvsContent;
	private int vvsAvailable;
	
	public int getVvsID() {
		return vvsID;
	}
	public void setVvsID(int vvsID) {
		this.vvsID = vvsID;
	}
	public String getVvsTitle() {
		return vvsTitle;
	}
	public void setVvsTitle(String vvsTitle) {
		this.vvsTitle = vvsTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getVvsDate() {
		return vvsDate;
	}
	public void setVvsDate(String vvsDate) {
		this.vvsDate = vvsDate;
	}
	public String getVvsContent() {
		return vvsContent;
	}
	public void setVvsContent(String vvsContent) {
		this.vvsContent = vvsContent;
	}
	public int getVvsAvailable() {
		return vvsAvailable;
	}
	public void setVvsAvailable(int vvsAvailable) {
		this.vvsAvailable = vvsAvailable;
	}

}
